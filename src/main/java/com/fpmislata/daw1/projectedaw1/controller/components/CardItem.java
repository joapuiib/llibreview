package com.fpmislata.daw1.projectedaw1.controller.components;

public class CardItem {
    private String titol;
    private String subtitol;
    private String url;
    private String imatgeUrl;

    public CardItem() {
    }

    public String getTitol() {
        return titol;
    }

    public void setTitol(String titol) {
        this.titol = titol;
    }

    public String getSubtitol() {
        return subtitol;
    }

    public void setSubtitol(String subtitol) {
        this.subtitol = subtitol;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getImatgeUrl() {
        return imatgeUrl;
    }

    public void setImatgeUrl(String imagtgeUrl) {
        this.imatgeUrl = imagtgeUrl;
    }
}
